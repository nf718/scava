package org.eclipse.scava.plugin.recommendation;

public class SourceCodeUpdaterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5343511142258585623L;

	public SourceCodeUpdaterException() {
		super();
		
	}

	public SourceCodeUpdaterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public SourceCodeUpdaterException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public SourceCodeUpdaterException(String message) {
		super(message);
		
	}

	public SourceCodeUpdaterException(Throwable cause) {
		super(cause);
		
	}
	
}
